public class SwitchCase
{       public static void main(String[] args)
          {
        int button= 1; 
        switch (button) {
            
            case 1:
                System.out.println("You picked NTR");
                break;

            case 2:
                System.out.println("You picked the Pawan Kalyan");
                break;

            case 3:
                System.out.println("You picked the Prabhas");
                break;

            default:
                System.out.println("none of movie heroes ");
                break;
        }
    }
}